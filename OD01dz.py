### Алгоритм для определения, является ли строка палиндромом:
# 1. ** Удалить
# все пробелы и привести строку к единому регистру. **
# - Это необходимо для того, чтобы сравнение было нечувствительным к регистру и пробелам.
# 2. ** Создать обратную версию строки. **
# - Это можно сделать с помощью срезов или циклов.
# 3. ** Сравнить оригинальную обработанную строку с её обратной версией. **
# - Если они совпадают, строка является палиндромом.
# 4. ** Вернуть результат проверки. **

### Функция для проверки, является ли строка палиндромом:



def is_palindrome(s: str) -> bool:
    # 1. Удаляем пробелы и приводим к нижнему регистру
    s = s.replace(" ", "").lower()

    # 2. Создаём обратную версию строки
    reversed_s = s[::-1]

    # 3. Сравниваем оригинальную строку с обратной
    if s == reversed_s:
        return True
    else:
        return False


# Пример использования
print(is_palindrome("А роза упала на лапу Азора"))  # Должно вернуть True